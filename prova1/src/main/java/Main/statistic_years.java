package Main;

public class statistic_years {
	public int year;
	public double mean;
	public double min;
	public double max;
	public double dev_std;
	public double sum;
	public long count;

	public statistic_years(int year, double mean, double min, double max, double dev_std, double sum, long count) {
		this.year = year;
		this.mean = mean;
		this.min = min;
		this.max = max;
		this.dev_std = dev_std;
		this.sum = sum;
		this.count = count;
	}
}


